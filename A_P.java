// a program to find the area and perimeter of a rectangle
class A_P
{
    public static void main(int l, int b)
    {
        double area, perimeter;
        area = l*b;
        perimeter = 2*(l+b);
        System.out.println("Area = "+area+"sq units");
        System.out.println("Perimeter = "+perimeter+"sq units");
    }
}