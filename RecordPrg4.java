class RecordPrg4 {
    public static void main(String args[]) {
        int a = 30, b = 3;
        int sum, diff, pro, sub;
        sum = a + b;
        diff = a / b;
        pro = a * b;
        sub = a - b;
        System.out.println("Sum of 30 and 3 is = " + sum);
        System.out.println("Differnece of 30 and 3 = " + diff);
        System.out.println("Product of 30 and 3 = " + pro);
        System.out.println("Subtraction of 30 and 3 = " + sub);
    }
}