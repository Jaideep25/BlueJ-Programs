// a program to find the sum and average of the following numbers
class Avg {
    public static void main(String args[]) {
        int x = 12, y = 8, z = 17, sum;
        double avg;
        sum = x + y + z;
        avg = sum / 3.0;
        System.out.println("Sum of x, y and z =" + sum);
        System.out.println("Average of x, y and z =" + avg);
    }
}