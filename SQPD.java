// A program to find the sum, difference, product and quotient
class SQPD {
    public static void main(int x, int y) {
        int sum, diff, pro, quo;
        sum = x + y;
        diff = x - y;
        pro = x * y;
        quo = x / y;
        System.out.println("Sum of x and y = " + sum);
        System.out.println("Difference of x and y = " + diff);
        System.out.println("Product of x and y = " + pro);
        System.out.println("Quotient between x and y = " + quo);
    }
}